###########################################################################################################################################

# Three types of web frameworks in Python:

# 1. Full-stack
# -> form generators/ template layouts
# -> template layouts
# -> http request handling
# -> WSGI Interfaces
# -> database connection handling
# -> ex: Django, Web2py, Pyramid


# 2. Microframeworks
# -> lighter version, small web projects
# -> ex: Flask, Bottle, Dash, CherryPi


# 3. Asynchronous
# -> used to handle a large sets of concurrent connections
# -> mainly built using Async IO networking libraries
# -> ex: Growler, AIOHTTP, Sanic

###########################################################################################################################################

