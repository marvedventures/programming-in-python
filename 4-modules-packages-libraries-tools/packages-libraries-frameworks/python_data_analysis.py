# PYTHON DATA ANALYSIS PACKAGES
###########################################################################################################################################

# 1. Scikit-Learn
# -> predictive-learning
# -> supervised, unsupervised ml algorithms for classification, regression, and SVM
# -> modelling data : clustering, feature extraction, selection, validation and dimensionality reduction

###########################################################################################################################################

# 2. Pandas
# -> acronym for Python Data Analysis
# -> used primarily for working with datasets and provides functions for cleaning, analyzing, and manipulating data.
# -> common applications: reading CSV files, reading JSON object
# -> primary data structure: Series(single dimensional), DataFrames(multi dimensional)

###########################################################################################################################################

# 3. NumPy
# -> NumericalPython
# -> powerful library forming the base for libraries such as Scikit-learn, Scipy, Plotly, and Matplotlib
# -> working with scientific domains such as signal, image processing, statistical computing, and quantum computing.
# -> carries out the calculations needed for algebraic areas such as Fourier transforms and matrices
# -> backbone data structure: ND array or N-dimensional array
# -> dimensions are called axes ; number of axes are called ranks

###########################################################################################################################################

# 4. Matplotlib
# -> visualization library
# -> can be used to create static, interactive and animated visualizations

