# BIG DATA AND ANALYSIS WITH PYTHON
###########################################################################################################################################

# Big data
# -> management of large sets of data, both structured and unstructured
# -> stored in the form of data warehouses and data lakes, both on servers and in the cloud

###########################################################################################################################################

# Characteristics that are commonly identified for use of Big Data tools:

# 1. Volume
# -> size of data under question and, if large enough, may require different handling to traditional data storage and management.

# 2. Variability or veracity
# -> inconsistency that may be present in this data

# 3. Velocity
# -> speed of handling this data

###########################################################################################################################################

# Big Data Advantages:
# 1. Ease of use
# 2. Licensing structure and open-source nature:
# 3. Active community
# 4. Libraries
# 5. High compatibility with Hadoop and Spark
# 6. High processing speed
# 7. Portability and scalability
# 8. Python tools and libraries -> Numpy, Pandas, Scikit-learn and Scipy

###########################################################################################################################################

# Libraries that are more specific to a Big Data domain such as:
# 1. RedShift and S3 -> Amazon services are used with their cloud services. S3 is a storage service and RedShift is a data warehousing service.
# 2. BigQuery -> Developed by Google, BigQuery is a Cloud service library that is useful with RESTful APIs
# 3. PySpark -> open-source framework used for large scale data processing and works with resilient distributed datasets.
# 4. Kafka ->  publish-subscribe messaging system that receives logs in the form of packages and is stored in partitioned spaces
# 5. Pydoop -> provides an interface between Hadoop and Python and support for handling its Hadoop distributed file systems.

