# DIFFERENT METHODS TO IMPORT

# NOTE: ALL WILL OUPUT THE SAME THING: 3.0
###########################################################################################################################################

# 1. import

import math
print(math.sqrt(9))

###########################################################################################################################################

# 2. from module import ...

from math import sqrt

print(sqrt(9))


###########################################################################################################################################

# 3. as

from math import sqrt as square_root

print(square_root(9))

###########################################################################################################################################

# 4. star * : import all

from math import *

print(sqrt(9))

