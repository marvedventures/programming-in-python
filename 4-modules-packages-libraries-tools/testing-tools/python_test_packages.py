# TEST AUTOMATION PACKAGES

# 1. Unittest
# -> built-in testing package
# -> supports test automation, independent testing modules, and aggregation of tests into collections


# 2. PyTest
# -> Native Python Library
# -> supports functional test types : unit, integration, and end-to-end(system).
# -> supports parameterized testing : execute unit tests multiple times with different parameters passed
# -> supports parallel testing: generates HTML, XML, or plain texts reports
# -> supports other testing frameworks : Pyunit
# -> supports web frameworks: Flask, Django
# -> use in: API's, UI, database connections, other web apps

# 3. Robot
# -> popular primarily for its keyword-driven development capabilities
# -> keywords are used in test cases and can be predefined or user-defined
# -> supports acceptance testing
# -> supports Robotic Process Automation (RPA)
# -> supports Test-Driven Development(TDD)
# -> use in: android, API's , mainframes

# 4. Selenium
# -> web applications
# -> support browser and OS
# -> support browser-specific web drivers: testing functionalities like login, button clicks, and filling out forms
# -> multiple options:  test set to select the speed and execution of tests and it has an option to run specific tests or tests suites.

