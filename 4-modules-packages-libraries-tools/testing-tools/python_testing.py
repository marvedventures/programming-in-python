###########################################################################################################################################
# WHAT IS TESTING?
###########################################################################################################################################

# TESTING
# -> process of evaluating and verifying the various software applications and products in terms of performance, correctness, and completeness.
# -> identifies bugs, gaps, defects, missing requirements

###########################################################################################################################################

# EFFICIENCY OF TESTING

# -> to have the least tests written to find the largest number of defects

###########################################################################################################################################

# TESTING IMPORTANCE IN SOFTWARE DEV. LIFE CYCLE (SDLC) :

# 1. helps detect POOR DESIGN
# 2. change inefficient flow or FUNCTIONALITY
# 3. address SCALABILITY concerns
# 4. find SECURITY vulnerabilities
# 5. AB TESTING to find suitable options
# 6. address COMPATIBILITY with platforms and devices
# 7. provide ASSURANCE to stakeholders
# 8. better EXPERIENCE to end-users

###########################################################################################################################################

# GOOD PRACTICE IN TESTING:

# 1. REUSABLITY : reusability of tests
# 2. TRACEABILITY : traceable to the requirements set
# 3. EFFICIENCY : must be purpose-driven, efficient, and allow for repeatability

###########################################################################################################################################

# TESTING LIFE-CYCLE

# 1. PLANNING
# 2. PREPARATION
# 3. EXECUTION
# 4. REPORTING

###########################################################################################################################################

# STEPS INVOLVED IN ACHIEVING TESTING LIFE-CYCLE

# 1. WRITING : writing scripts and test cases
# 2. COMPILING : compiling test results
# 3. CORRECTING : correcting defects based on test results
# 4. REPORTING : generating reports from test results.

###########################################################################################################################################

# WELL WRITTEN TEST CASES CAN PROVIDE:

# 1. COVERAGE
# 2. REUSABILITY
# 3. USER EXPERIENCE
# 4. REDUCE COST
# 5. INCREASE OVERALL SATISFACTION

###########################################################################################################################################

# TESTING TYPES

# 1. COMPATIBILITY
# 2. AD HOC
# 3. USABILITY
# 4. REGRESSION TESTING

